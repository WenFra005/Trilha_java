package Java_básico.Repetição;
public class ComandoFor {
   //Contegem de carneirinhos usando o comando 'for'
    public static void main(String[] args) {
        for (int carneirinhos = 1; carneirinhos <=20; carneirinhos++){
            System.out.println("Contando " + carneirinhos + " carneirinho(s)");
        }
    }
}
